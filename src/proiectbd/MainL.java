      public ArrayList<Lucrare> getLucrareList(){
             ArrayList<Lucrare> lucrareList= new ArrayList<Lucrare>();
            Connection con= getConnection();
            String query = "SELECT * FROM lucrare";
            
            Statement st;
            ResultSet rs;
        try {
            
            
            st = con.createStatement();
            rs = st.executeQuery(query);
            Lucrare lucrare;
            while(rs.next()){
                lucrare = new Lucrare(rs.getInt("nr_l"),rs.getString("disciplina"),rs.getString("data"),rs.getFloat("nota"),rs.getInt("nr_e"),rs.getInt("nr_c"));
                lucrareList.add(lucrare);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainSc.class.getName()).log(Level.SEVERE, null, ex);
        }
        return lucrareList;
        
    }
    
    
    public void Show_Lucrare(){
        ArrayList<Lucrare> list = getLucrareList();
        DefaultTableModel model= (DefaultTableModel)tbl_lucrare.getModel();
        
        model.setRowCount(0);
        Object[] row = new Object[6];
        for (int i= 0;i< list.size();i++){
            row[0]= list.get(i).getLnr();
            row[1]= list.get(i).getLdisciplina();
            row[2]= list.get(i).getLdata();
            row[3]= list.get(i).getLnota();
            row[4]= list.get(i).getLnre();
            row[5]= list.get(i).getLnrc();
            
         
            
            model.addRow(row);
            
            
        }
        
    }
    
    
