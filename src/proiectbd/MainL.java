    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed

        if(checkInputs() && txt_nrl !=null){
            Connection con=getConnection();

            try {
                PreparedStatement ps=con.prepareStatement("INSERT INTO lucrare (disciplina,data,nota,nr_e,nr_c)"
                    +" values (?,?,?,?,?);");
                
                ps.setString(1, combo_disc.getSelectedItem().toString());
                ps.setString(2,txt_data.getText());
                ps.setString(3,txt_nota.getText());
                ps.setString(4,txt_nreL.getText());
                ps.setString(5,txt_nrcL.getText());
                ps.executeUpdate();
                Show_Lucrare();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Unul sau mai multe campuri sunt goale");
        }
        
    }//GEN-LAST:event_btn_insertActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        if(checkInputs() && txt_nrl.getText()!= null){
            String update= null;
            PreparedStatement ps=null;
            Connection con= getConnection();
            try{
                update="UPDATE lucrare SET disciplina =?, data = ?, nota = ?,nr_e =?, nr_c=? WHERE nr_l = ?";
                ps=con.prepareStatement(update);
                ps.setString(1, combo_disc.getSelectedItem().toString());
                ps.setString(2, txt_data.getText());
                ps.setString(3, txt_nota.getText());
                ps.setString(4 ,txt_nreL.getText());
                ps.setString(5 ,txt_nrcL.getText());
                ps.setInt(6, Integer.parseInt( txt_nrl.getText()));
                ps.executeUpdate();
                Show_Lucrare();}
            catch(SQLException ex){
                Logger.getLogger(MainSc.class.getName()).log(Level.SEVERE,null,ex);
            }
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        if(!txt_nrl.getText().equals("")){
            try {
                Connection con= getConnection();
                PreparedStatement ps= con.prepareStatement("DELETE FROM lucrare WHERE nr_l = ?");
                int nr_l= Integer.parseInt(txt_nrl.getText());
                ps.setInt(1, nr_l);
                ps.executeUpdate();
                Show_Lucrare();
                JOptionPane.showMessageDialog(null,"Lucrarea a fost stearsa.");

            } catch (SQLException ex) {
                Logger.getLogger(MainSc.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null,"Lucrarea nu a fost stearsa.");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Lucrarea nu a fost stearsa, introduceti numarul lucrarii.");
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

